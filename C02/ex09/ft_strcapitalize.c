#include <unistd.h>

int	non_alphanum(char c)
{
	return ((c == ' ' || c == ',' || c == ';')
		|| (c == '.' || c == '!' || c == '?')
		|| (c == '\"' || c == '\'' || c == '(')
		|| (c == ')' || c == '+' || c == '-'));
}

char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (i == 0 || non_alphanum(str[i - 1]))
		{
			if (str[i] >= 97 && str[i] <= 122)
			{
				str[i] -= 32;
			}
		}
		else if (str[i] > 65 && str[i] <= 90)
		{
			str[i] += 32;
		}
		i++;
	}
	return (str);
}
