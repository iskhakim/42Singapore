#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	buff[12];
	int		i;
	long	n;

	i = 0;
	n = nb;
	if (n < 0)
	{
		write (1, "-", 1);
		n = -n;
	}
	if (n == 0)
	{
		write (1, "0", 1);
	}
	while (n > 0)
	{
		buff[i++] = n % 10 + '0';
		n /= 10;
	}
	while (i--)
	{
		write (1, &buff[i], 1);
	}
}
